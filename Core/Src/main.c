void do_fft(uint8_t hadc_flag) {
  if (hadc_flag) {

      arm_rfft_fast_instance_f32 fft_instance;
      
      float32_t fft_adc_buf[ADC_BUF / 2] = {0};
      float32_t fft_output[FFT_SIZE * 2] = {0};
      float32_t fft_magnitude[FFT_SIZE] = {0};

      volatile float32_t calculated_frequency = 0;
      volatile float32_t max_value;
      volatile uint32_t max_index;

      int i, j;
      for (i = 0, j = 1; i < ADC_BUF; i++, j+=2) {
          fft_adc_buf[i] = (float32_t)data_i2s[j];
      }

      
      
      arm_rfft_fast_init_f32(&fft_instance, ADC_BUF / 2);
      arm_rfft_fast_f32(&fft_instance, fft_adc_buf, fft_output, 0);

      arm_cmplx_mag_f32(fft_output, fft_magnitude, FFT_SIZE);
      fft_magnitude[0] = 0;
      fft_magnitude[1] = 0;
      arm_max_f32(fft_magnitude, FFT_SIZE / 2, &max_value, &max_index);

      uint32_t temp_max_index = 0;
      float32_t temp_max_value = 0;
      max_index = 0;
      for (int v = 20; v < 22; v++) {
          temp_max_index = 0;
        if (fft_magnitude[v] > temp_max_value) {
          temp_max_value = fft_magnitude[v];
          temp_max_index = v;
        }
      }
      calculated_frequency = temp_max_index * (16000.0 / (FFT_SIZE * 1.0));
          //if (calculated_frequency >= 80) {
          sprintf((char*)buf,
            "%u Hz\r\n",
            ((unsigned int)calculated_frequency));
          HAL_UART_Transmit(&huart2,buf,strlen((char*)buf),10);// Sending in normal mode

      // calculated_frequency = max_index * (16000.0 / (FFT_SIZE * 1.0));

      //if (calculated_frequency >= 80) {
        sprintf((char*)buf,
                  "%u Hz\r\n",
                  ((unsigned int)calculated_frequency));
      HAL_UART_Transmit(&huart2,buf,strlen((char*)buf),10);// Sending in normal mode

      hsai_flag = 0;
    }
}
